// Дефинирайте функция за ограничаване (clamp-ване) на число отгоре - функцията трябва да приема два float параметъра и да
// върне стойност, която е равна на първия параметър или на втория, в случай че първия е по-голям от втория

float clampHigh (float x, float upperBorder)
{
    return x > upperBorder ? upperBorder : x;
}