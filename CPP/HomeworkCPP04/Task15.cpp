// Дефинирайте функция, която по зададен индекс в редицата на Фибоначи, връща числото от редицата, съответстваща на
// подадения индекс. Използвайте рекурсивен алгоритъм

int fibionacciIndex(int x){
    if(x == 1 || x ==2){
        return 1;
    }
    return fibionacciIndex(x-1) + fibionacciIndex(x-2);
}